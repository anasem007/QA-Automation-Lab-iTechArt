using System;

namespace NUnitLearning
{
    public class Calculator
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Div(int a, int b)
        {
            return a / b;
        }
        
        public Boolean Compare(int a, int b)
        {
            return a > b;
        }
    }
}